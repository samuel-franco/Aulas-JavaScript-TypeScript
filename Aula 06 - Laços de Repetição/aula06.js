// Laços de repetição: for, While, Do While

// for
for (let contador = 0; contador < 4; contador++) {
    console.log(contador)
    console.log("Aumentar contador")  
};


let pontosDeVida = 0;

for (let i = 1; i <= 10; i++) {
        pontosDeVida += 1
        console.log("Tomou poção mágica " + i)
};

console.log(pontosDeVida + " Totais ");